use anchor_lang::prelude::*;

/// Помилки програми Search
#[error_code]
pub enum SearchError {
    /// Таймаут пошуку ще не минув (потрібно зачекати 60 секунд)
    #[msg("Таймаут пошуку ще не минув: зачекайте 60 секунд між пошуками")]
    CooldownNotElapsed,
    /// Неавторизований виклик: гравець не є власником акаунту
    #[msg("Доступ заборонено: гравець не є власником цього акаунту")]
    UnauthorizedAdmin,
    /// SearchAuthority вже ініціалізовано
    #[msg("SearchAuthority вже ініціалізовано")]
    AlreadyInitialized,
    /// Переданий мінт ресурсу не відповідає псевдовипадковому вибору
    #[msg("Переданий мінт ресурсу не відповідає псевдовипадковому вибору")]
    InvalidResourceMint,
}
