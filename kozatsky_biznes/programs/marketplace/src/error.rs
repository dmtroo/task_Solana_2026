use anchor_lang::prelude::*;

/// Помилки програми Marketplace
#[error_code]
pub enum MarketplaceError {
    /// Недійсний тип предмету (0-3)
    #[msg("Недійсний тип предмету для продажу (0-3)")]
    InvalidItemType,
    /// NFT мінт не відповідає ItemMetadata
    #[msg("NFT мінт не відповідає збереженим метаданим предмету")]
    MintMismatch,
    /// Тип предмету не відповідає очікуваному
    #[msg("Тип предмету NFT не відповідає вказаному при продажу")]
    ItemTypeMismatch,
    /// Неавторизований адміністратор
    #[msg("Доступ заборонено: не адміністратор")]
    UnauthorizedAdmin,
    /// Ціна предмету не встановлена
    #[msg("Ціна предмету не встановлена в конфігурації гри")]
    PriceNotSet,
}
